BOUNDARIES: dict[int, np.ndarray] = {
    bits: (cb[:-1] + cb[1:]) / 2.0
    for bits, cb in CODEBOOKS.items()
}

SUPPORTED_BITS = frozenset(CODEBOOKS.keys())


def get_codebook(bits: int) -> tuple[np.ndarray, np.ndarray]:
    """Return (codebook, boundaries) for the given bit width.

    Returns:
        codebook:   float32 array of shape (2**bits,)  — centroids in ascending order
        boundaries: float32 array of shape (2**bits-1,) — midpoints for np.digitize
    """
    if bits not in CODEBOOKS:
        raise ValueError(f"Unsupported bit width {bits}. Choose from {sorted(SUPPORTED_BITS)}")
    return CODEBOOKS[bits], BOUNDARIES[bits]
